package com.company;

public class Car {
    private String make;
    private String model;
    private int year;

    public void setMake(String myMake) {
        make = myMake;
    }

    public String getMake() {
        return make;
    }

    public void setModel(String myModel) {
        model = myModel;
    }

    public String getModel() {
        return model;
    }

    public void setYear(int carYear) {
        year = carYear;
    }

    public int getYear() {
        return year;
    }


}
